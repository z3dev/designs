function getBottleDimensions(p) {
    p.bottle_h = 290.0;
    p.bottle_b = 200.0;
    p.bottle_d = 75.0;
    p.bottle_n = 30.0;
    p.bottle_r = p.bottle_d / 2;
    p.bottle_c = [139 / 255, 69 / 255, 19 / 255, 0.7];
    p.bottle_l = 0.750 * 1000000; // volumne of contents mm3
}

function createIce(p) {
    let x = p.ice_wr;
    let y = p.ice_h;
    let o = p.ice_ir + x;
    let rr = p.ice_wr * 0.50;
    let i = CAG.roundedRectangle({center: [o,0], radius: [x,y], roundradius: rr, resolution: p.resolution});
    i = i.rotateExtrude({offset: [50,50,50]}); /* angle: 360, resolution: 12 */
    let s = y;
    x = p.ice_w;
    let b = CSG.cylinder({start: [0,0,-s], end: [0,0,-s+x], radius: o, resolution: 12});
    i = i.union(b).translate([0,0,y]);
    return i;
}

