function createBase(p) {
    // volume = π × r2 × h
    let ov = Math.PI * (p.ice_or * p.ice_or) * p.ice_h;
    let iv = Math.PI * (p.ice_ir * p.ice_ir) * (p.ice_h - p.ice_w);
    let v = (ov - iv) * 1.10;
    console.log(v);
    let r = p.ice_or + 20.0;
    console.log(r);
    let z = v / (Math.PI * r * r);
    console.log(z);
    let inner = CSG.cylinder({start: [0,0,0], end: [0,0,-z], radius: r, resolution: p.resolution});
    inner = inner.translate([0,0,-p.base_t]);

    r = r + p.base_t;
    z = z + (p.base_t * 2);
    let outer = CSG.cylinder({start: [0,0,0], end: [0,0,-z], radius: r, resolution: p.resolution});

    r = p.ice_ir - p.ice_w;
    let drain = CSG.cylinder({start: [0,0,0], end: [0,0,-z], radius: r, resolution: p.resolution});
    drain = drain.translate([0,0,p.base_t]);

    let base = outer.subtract(inner);
    base = base.subtract(drain);
    base = base.setColor([70 / 255, 130 / 255, 180 / 255, 0.7]);
    return base;
}

// Note: Set p.drain = true or false
function createDrainOrPlug(p) {
    let z = p.base_t / 2;
    let r = p.ice_wr;
    let outer = CSG.cylinder({start: [0,0,z], end: [0,0,z+z], radius: r, resolution: p.resolution});
    r = p.ice_wr - p.base_t;
    let inner = CSG.cylinder({start: [0,0,0], end: [0,0,z+z], radius: r, resolution: p.resolution});

    let drain = outer.union(inner);
    
    z = (p.ice_h * 0.75) + p.ice_w;
    r = p.ice_wr - p.base_t;
    let pipe = CSG.roundedCylinder({start: [0,0,0], end: [0,0,z], radius: r, resolution: p.resolution});
    drain = drain.union(pipe);

    x = p.ice_ir;
    z = p.base_t;
    drain = drain.translate([x,0,-z]);
    return drain;
}

function createBottle(p) {
    let z = p.bottle_h;
    let r = p.bottle_r;
    let b = CSG.cylinder({start: [0,0,0], end: [0,0,z], radius: r});
    b = b.translate([0,0,p.ice_w]);
    b = b.setColor(p.bottle_c);
    return b;
}

function main(p) {
    getBottleDimensions(p);
    
    let view = [];
    
    p.base_t = 4.0;
    
    p.ice_h = (p.bottle_b / 2 / 2); // half of the body height
    p.ice_w = 15.0;
    p.ice_wr = p.ice_w / 2;
    p.ice_h = p.ice_h + p.ice_w;
    p.ice_ir = p.bottle_r + 1.0;
    p.ice_or = p.ice_ir + (p.ice_w * 2);

    if (p.bottle_v) {
        let bottle = createBottle(p);
        view.push(bottle);
    }

    if (p.ice_v) {
        let ice = createIce(p);
        let plug = createDrainOrPlug(p);
        ice = ice.subtract(plug);
        ice = ice.setColor([127 / 255, 255 / 255, 212 / 255, 0.4]);
        view.push(ice);
    }

    if (p.base_v) {
        let base = createBase(p);
        let plug = createDrainOrPlug(p);
        base = base.subtract(plug).union(plug);
        view.push(base);
    }
    
    return view;
}

